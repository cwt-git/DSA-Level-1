
// How to print in java

public class how_to_print {

    public static void main(String[] args) {

        System.out.println("This line will print in Java");

        System.out.print("This line will print in Java");

        // When using println, it moves to the next line for the following command.
        // but both will print this
        System.out.println(); // this blank will also do same Work as "/n".
        // Print Triangle
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");

        // Other way
        System.out.println("*\n**\n***\n****\n*****");
    }
}