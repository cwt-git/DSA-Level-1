public class HowToPrint {

    public static void main(String[] args) {
        
        // Print a line with a newline character at the end
        System.out.println("This line will print in Java");
        
        // Print a line without a newline character at the end
        System.out.print("This line will print in Java");
        
        // Print a blank line (equivalent to \n)
        System.out.println();
        
        // Print a triangle with each line using println
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        
        // Print a triangle with each line using \n for new lines
        System.out.println("*\n**\n***\n****\n*****");
    }
}
