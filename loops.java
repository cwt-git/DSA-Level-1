public class loops {
    public static void main(String[] args) {
        // for Loop
        int n = 15;
        for (int i = 0; i < n; i++) {
            System.out.println("hello" + i);
        }
        // While loop
        int i = 1;
        while (i <= 9) {
            System.out.println(i);
            i++;
        }
        System.out.println("I am out of while loop");
    }
}
