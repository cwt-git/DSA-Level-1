public class printZ {
    public static void main(String[] args) {
        // Print the top horizontal line of 'Z'
        System.out.println("*****");
        
        // Print the diagonal part of 'Z'
        System.out.println("   * ");
        System.out.println("  *  ");
        System.out.println(" *   ");
        
        // Print the bottom horizontal line of 'Z'
        System.out.println("*****");
    }
}
